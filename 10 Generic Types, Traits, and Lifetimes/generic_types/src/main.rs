// <T> resembles "any" type, which makes this function functional in multiple types.
fn largest<T: std::cmp::PartialOrd>(list: &[T]) -> &T {
    let mut largest = &list[0];

    for item in list {
        if item > largest {
            largest = item;
        }
    }

    largest
}

fn main() {
    let number_list = vec![34, 50, 25, 100, 65];

    let result = largest(&number_list);
    println!("The largest number is {}", result);

    // As expected, characters that contain multiple letters are bigger. (Because of byte sizes)
    let char_list = vec!['y', 'm', 'a', 'は'];

    let result = largest(&char_list);
    println!("The largest char is {}", result);
}